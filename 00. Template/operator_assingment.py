# operasi ditambahkan dengan assignment
a = 5 
print ("valor a=", a)

a += 1 # signifika a= a+1
print ("valor a+=, valor a mak:",a)

a -= 2 # signifika a= a-2
print ("valor a-=2, valor a mak:",a)

a *= 5 # signifika a= a*5
print ("valor a*=2, valor a mak:",a)

a /= 2 # signifika a= a/2
print ("valor a/=2, valor a mak:",a)

